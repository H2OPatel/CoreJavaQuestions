package ArrayPracticeAtApex;

public class FirstArrayProgram {

	public static void main(String[] args) {
		
			int[]arrayNums = new int[]{10,2,3,8,69,56,29,68,22,21};
			System.out.println("Length of the array:"+arrayNums.length);
			for(int index=0; index<arrayNums.length;index++){
				System.out.println("Value at index "+index +" is "+arrayNums[index]);
			}

		

	}

}
