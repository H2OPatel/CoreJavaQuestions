
public class BasicCalculator {
	public int addition(int var1 , int var2) {
		
		return var1 + var2;
		
	}
	public int subtraction(int var1 , int var2) {
		
		return var1 - var2;
		
	}
	public int multiplication(int var1 , int var2) {
	
	return var1 * var2;
	
	}
	public int division(int var1 , int var2) {
	
	return var1 / var2;
	
	}


}
